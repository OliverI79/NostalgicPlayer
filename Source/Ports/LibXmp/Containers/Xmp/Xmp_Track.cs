namespace Polycode.NostalgicPlayer.Ports.LibXmp.Containers.Xmp
{
	/// <summary>
	/// 
	/// </summary>
	public class Xmp_Track
	{
		/// <summary>
		/// Number of rows
		/// </summary>
		public c_int Rows;

		/// <summary>
		/// Event data
		/// </summary>
		public Xmp_Event[] Event;

		/********************************************************************/
		/// <summary>
		/// 
		/// </summary>
		/********************************************************************/
		public void Clear()
		{
			Rows = 0;

			foreach (Xmp_Event e in Event)
				e.Clear();
		}
	}
}
